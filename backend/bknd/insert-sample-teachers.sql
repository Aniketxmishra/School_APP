    fdstaffcode as 'Staff Code',
    CONCAT(fdfirstname, ' ', fdlastname) as 'Full Name',
    fdgender as 'Gender',
    fdmobile as 'Mobile',
    fdemail as 'Email',
    fdqualification as 'Qualification',
    fdexperience as 'Experience (Years)',
    fdjoiningdate as 'Joining Date',
    fdstatus as 'Status'
FROM tbmasteacher 
